    private void btn_DéplacéActionPerformed(java.awt.event.ActionEvent evt) {                                            
        // TODO add your handling code here:
        
        if((txt_date_r.getText().equals("")) || (annexe.getSelectedItem().toString().equals("")) || (txt_nom.getText().equals(""))){
            JOptionPane.showMessageDialog(this,"Veuillez remplir tous les champs svp !!!", "Erreur",JOptionPane.ERROR_MESSAGE);
        }else{
            int q = Integer.parseInt(qte.getSelectedItem().toString());
            Deplacement dpl = new Deplacement(this.code,txt_date_dpl.getText(),txt_date_r.getText(),annexe.getSelectedItem().toString(),q,txt_nom.getText());
            String rep = dpl.deplacer_Article();
            if (rep.equalsIgnoreCase("code invalid")){
                JOptionPane.showMessageDialog(this,"Le code d'article est invalid", "Erreur",JOptionPane.ERROR_MESSAGE);
            }else
                if(rep.equalsIgnoreCase("qte deplace superieur")){
                    JOptionPane.showMessageDialog(this,"La quantite deplacée doit etre inferieur a la quantite stockée", "Erreur",JOptionPane.ERROR_MESSAGE);
                }else
                if(rep.equalsIgnoreCase("date invalide")){
                    JOptionPane.showMessageDialog(this,"La date de retour doit etre superieur a la date de deplacement !!!", "Erreur",JOptionPane.ERROR_MESSAGE);
                }else
                    if(rep.equalsIgnoreCase("echec")){
                    JOptionPane.showMessageDialog(this,"Deplacement echoué !!!", "Erreur",JOptionPane.ERROR_MESSAGE);
                }else{
                    JOptionPane.showMessageDialog(this,"Deplacement effectué avec succes !!!", "Erreur",JOptionPane.INFORMATION_MESSAGE);
                    btn_Déplacé.setEnabled(false);
                    btn_imp.setEnabled(true);
                    
                }
        }
    }                                           

