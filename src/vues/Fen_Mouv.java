    private void btn_dspActionPerformed(java.awt.event.ActionEvent evt) {                                        
        // TODO add your handling code here:
        String nom  =  JOptionPane.showInputDialog(null, "Veuillez entrer le code d'article a rechercher : ", "Rechercher Articles", JOptionPane.QUESTION_MESSAGE);
         Deplacement dpl = new Deplacement();
         if ((nom == null) || (nom.equalsIgnoreCase(""))){
              
            }else{
                       ArrayList dsp =  dpl.Disponibilite_art(nom);
                        //JOptionPane.showMessageDialog(this,dsp.size(), "Erreur",JOptionPane.ERROR_MESSAGE);
                       
                      if (dsp == null){
                           JOptionPane.showMessageDialog(this,"quantite indisponible !!!", "Erreur",JOptionPane.ERROR_MESSAGE);
                      }else
                        if(dsp.get(0).toString().equalsIgnoreCase("")){
                            JOptionPane.showMessageDialog(this,"code invalid !!!", "Erreur",JOptionPane.ERROR_MESSAGE);
                      }else{
                          lb_code_art.setText(dsp.get(0).toString());  
                          this.code = dsp.get(0).toString();
                          lb_nom_art.setText(dsp.get(1).toString());
                          lb_qte.setText(dsp.get(4).toString());
                          lb_cat.setText(dsp.get(2).toString());
                          lb_etat.setText(dsp.get(3).toString());
                             
                          btn_Déplacé.setEnabled(true);
                        }
              }
    }                                       

    private void btn_annActionPerformed(java.awt.event.ActionEvent evt) {                                        
        // TODO add your handling code here:
        String rep = "";
       Deplacement dpl = new Deplacement();
       String code  =  JOptionPane.showInputDialog(null, "Veuillez entrer le code du deplacement à annuler : ", "Annuler un deplacement",JOptionPane.QUESTION_MESSAGE);
       if ((code == null) || (code.equalsIgnoreCase(""))){
        
        }else{
            int option = JOptionPane.showConfirmDialog(null, "Voulez-vous vraiment supprimer l'article ?", "Lancement de la suppression", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if ((option == JOptionPane.NO_OPTION) || (option == JOptionPane.CANCEL_OPTION) || (option == JOptionPane.CLOSED_OPTION)){
                
            }else{
                String r = dpl.annuler_Deplacement(code);
                              
                if (r.equalsIgnoreCase("kod la pa bon !")){
                    JOptionPane.showMessageDialog(this,"Code invalide!!");
                }else
                    if (r.equalsIgnoreCase("succes")){
                        JOptionPane.showMessageDialog(this,"Deplacement annulé effectuée avec succes!!");
                        
                    }else{
                           JOptionPane.showMessageDialog(this,"Echec de l'annulation d'un deplacement!!!!");
                    }
                   
            }
        }
    }                                       

